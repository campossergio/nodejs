console.log("Operadores aritméticos");
console.log("+ | soma = 10 + 2");
console.log(10 + 2);
console.log("- | subtração = 10 - 2");
console.log(10 - 2);
console.log("* | multiplicação = 10 * 2");
console.log(10 * 2);
console.log("/ | divisão = 10 / 2");
console.log(10 / 2);
console.log("% | módulo (sobra da divisão) = 10 % 2");
console.log(10 % 2);
console.log("-------------------------------\n");
console.log("Operadores aritméticos de atribuição");
console.log("+= | soma e atribuição a variável");
console.log("-= | subtração e atribuição a variável");
console.log("*= | multiplicação e atribuição a variável");
console.log("/= | divisão e atribuição a variável");
console.log("++ | soma 1 ao resultado");
console.log("-- | subtrai 1 do resultado");
console.log("---------------------------\n");
console.log("Operadores de comparação");
console.log(">   | maior que");
console.log(">=  | maior ou igual a");
console.log("<   | menor que");
console.log("<=  | menor ou igual a");
console.log("==  | igual a");
console.log("!=  | diferente de");
console.log("=== | idêntico a");
console.log("!== | não idêntico a");
console.log("--------------------------------\n");
console.log("Operadores lógicos");
console.log("&&  | AND");
console.log("||  | OR");
console.log("!   | NOT");
